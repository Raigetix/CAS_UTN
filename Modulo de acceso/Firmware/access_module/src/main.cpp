 void app_logo(void *logo, int color, const char* text, int pos_x, int pos_y, int width, int height) {
  const int radius = 10;              // radio de bordes redondeados
  const int padding = 10;             // margen interno
  const int text_height = 16;         // altura del texto
  const int icon_area_height = height - text_height - padding * 2;
  const int icon_size = min(width - padding * 2, icon_area_height); // se adapta al tamaño del contenedor

  // Fondo redondeado
  gfx->fillRoundRect(pos_x, pos_y, width, height, radius, color);

  // Dibuja el ícono centrado horizontalmente
  if (logo != nullptr) {
    int icon_x = pos_x + (width - icon_size) / 2;
    int icon_y = pos_y + padding;
    gfx->draw16bitRGBBitmap(icon_x, icon_y, (uint16_t*)logo, icon_size, icon_size);
  }

  // Texto centrado horizontalmente
  gfx->setTextColor(WHITE);
  gfx->setTextSize(1);
  int text_width = strlen(text) * 6; // estimación: 6 px por carácter
  int text_x = pos_x + (width - text_width) / 2;
  int text_y = pos_y + height - text_height;
  gfx->setCursor(text_x, text_y);
  gfx->print(text);
}



